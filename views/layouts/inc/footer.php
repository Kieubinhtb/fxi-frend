<script>
    let subcribeModal = $('#subcribe-modal');
    let formSubscribe = $("#subscribe-form").validate({
        rules: {
            'subcribe-email': {
                required: true,
                email: true
            }
        },
        errorPlacement: function(error, element) {
            element.parent().after(error);
        },
        messages: {
            'subcribe-email': {
                required: "Vui lòng nhập thông tin",
                email: "Định dạng email không đúng"
            }
        }
    });

    function validateEmail(email) {
        const re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/mgi;
        return re.test(String(email).toLowerCase());
    }

    $("#subscribe-form").on('submit', function(e) {
        e.preventDefault();
        const serializedData = $(this).serializeArray();
        let email = serializedData[0].value;
        if (email.length && validateEmail(email)) {
            $.ajax({
                type: 'POST',
                url: "/site/subscribe-email",
                data: {
                    'subscribe-email': email
                },
                success: function(response) {
                    let successContent = `<div class="notice-modal">
                    <div class="notice-modal__icon cl-primary"><i class="fa fa-check" aria-hidden="true"></i></div>
                    <div class="notice-modal__content text-center">
                        Đăng ký thành công !
                    </div>
                </div>`;

                    let errorContent = `<div class="notice-modal">
                    <div class="notice-modal__icon cl-red"><i class="fa fa-info-circle" aria-hidden="true"></i></div>
                    <div class="notice-modal__content text-center">
                        Email đã tồn tại!
                    </div>
                </div>`;
                    subcribeModal.find('.modal-body').empty();
                    let json = JSON.parse(response);
                    // true                 
                    subcribeModal.find('.modal-body').append(json.success ? successContent : errorContent);
                    // false subcribeModal.find('.modal-body').append(errorContent);
                    subcribeModal.modal('show');
                },
                error: function(response) {
                    console.log(response)
                }
            });
        }
    });

    $('#comment-page-form .rating').each(function() {
        let $this = $(this);
        let label = $('label', this);
        label.on('click', function() {
            let i = $(this).index() + 1;
            label.removeClass('active');
            $this.find('label:nth-child(-n+' + i + ')').addClass('active');
            $(this).parent().next('input').val(i);
        });
    });

    let commentPageForm = $('#comment-page-form');
    let commentPageFormValidate = commentPageForm.validate({
        ignore: "",
        rules: {
            message: {
                required: true
            },
            rating_layouts: "required",
            rating_contents: "required",
            rating_colors: "required",
            qt_hiddenRecaptcha: {
                required: true
            }
        },
        errorPlacement: function(error, element) {
            if (element.attr('type') == 'checkbox') {
                element.parent().after(error);
            } else {
                error.insertAfter(element);
            }
        },
        messages: {
            message: {
                required: "Vui lòng nhập thông tin"
            },
            rating_layouts: "Vui lòng đánh giá",
            rating_contents: "Vui lòng đánh giá",
            rating_colors: "Vui lòng đánh giá",
            qt_hiddenRecaptcha: "Vui lòng kiểm tra captcha"
        }
    });

    $("#comment-page-form").on('submit', function(e) {
        e.preventDefault();
        let fr = $(this);
        if (commentPageFormValidate.errorList.length < 1) {
            $.ajax({
                type: 'POST',
                url: "/site/submit-requirements",
                data: fr.serialize(),
                success: function(response) {
                    let successContent = `<div class="notice-modal">
                    <div class="notice-modal__icon cl-primary"><i class="fa fa-check" aria-hidden="true"></i></div>
                    <div class="notice-modal__content text-center">
                        Cảm ơn bạn đã gửi ý kiến đánh giá về chúng tôi !
                    </div>
                </div>`;

                    let errorContent = `<div class="notice-modal">
                    <div class="notice-modal__icon cl-red"><i class="fa fa-info-circle" aria-hidden="true"></i></div>
                    <div class="notice-modal__content text-center">
                        Hệ thống đang gặp sự cố. Xin thử lại sau!
                    </div>
                </div>`;
                    subcribeModal.find('.modal-body').empty();
                    let json = JSON.parse(response);
                    // true                 
                    subcribeModal.find('.modal-body').append(json.success ? successContent : errorContent);
                    // false subcribeModal.find('.modal-body').append(errorContent);
                    subcribeModal.modal('show');

                    $('#comment-page-modal').removeClass('shown-modal').addClass('hidden-modal');
                    $('body').removeClass('overflow-auto');
                    $('#comment-page-modal').modal('hide');
                },
                error: function(response) {
                    console.log(response)
                }
            });
        }
    });
</script>